// Alle Ausgaben nacheinander ausgaben als Showcase (Veraltet)
void emojiShowcase()
{
    if (millis() - startMillisEmoji <= 5000)
    {
        emojiHappy();
    }
    else if (millis() - startMillisEmoji <= 10000)
    {
        emojiMeh();
    }
    else if (millis() - startMillisEmoji <= 15000)
    {
        emojiSad();
    }
    else
    {
        startMillisEmoji = millis();
    }
}

